// Helper function to get terms by letter
export const getTermsByLetter = (letter) => {
  return legalGlossary.filter(term => 
    term.term.charAt(0).toUpperCase() === letter.toUpperCase()
  );
};

// Helper function to search terms
export const searchTerms = (query) => {
  const lowerQuery = query.toLowerCase();
  return legalGlossary.filter(term =>
    term.term.toLowerCase().includes(lowerQuery) ||
    term.hindi.includes(query) ||
    term.kannada.includes(query) ||
    term.definition.toLowerCase().includes(lowerQuery) ||
    term.category.toLowerCase().includes(lowerQuery)
  );
};

// Get all categories
export const getCategories = () => {
  const categories = [...new Set(legalGlossary.map(term => term.category))];
  return categories.sort();
};

// Get related terms
export const getRelatedTerms = (termName) => {
  const term = legalGlossary.find(t => t.term.toLowerCase() === termName.toLowerCase());
  if (!term) return [];
  
  return legalGlossary.filter(t => 
    term.relatedTerms.some(rt => 
      t.term.toLowerCase().includes(rt.toLowerCase()) ||
      rt.toLowerCase().includes(t.term.toLowerCase())
    )
  );
};

